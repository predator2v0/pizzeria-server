const mongoose = require("mongoose");

const userSchema = mongoose.Schema({
    userID: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    pincode: {
        type: Number,
        required: true,
    },
});
const userModel = mongoose.model("user", userSchema);

module.exports = userModel;
